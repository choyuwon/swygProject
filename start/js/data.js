// JavaScript 코드를 추가하여 HTML에 "command" 값을 동적으로 표시
// 페이지 로드 후 실행
document.addEventListener("DOMContentLoaded", function () {
  // 추천 알바 데이터 가져오기
  const recommendedJobs = infoList.map(item => item.command);

  // 결과를 표시할 HTML 엘리먼트를 찾습니다.
  const resultDescElement = document.querySelector('.resultDesc');

  // 추천 알바 데이터를 HTML에 추가합니다.
  for (let i = 0; i < recommendedJobs.length; i++) {
    const pElement = document.createElement('p');
    pElement.textContent = recommendedJobs[i];
    resultDescElement.appendChild(pElement);
  }
});